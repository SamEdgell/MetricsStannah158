        msg_ECU_2_ADC.append(0x00)

        self.main_window.ui_comms.handleMessage(msg_ECU_1_ADC)
        self.main_window.ui_comms.handleMessage(msg_ECU_2_ADC)

        self.demo_ADC_index = (self.demo_ADC_index + 1) % len(self.ecu1_ADC_payloads)


